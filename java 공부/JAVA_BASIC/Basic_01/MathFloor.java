public class MathFloor {
  public static void main(String[] args) {
    // Math 관련 메소드 실습
    System.out.println(" 값 : " + Math.floor(65.6));
    System.out.println(" 값 : " + Math.ceil(65.1));
    System.out.println(" 값 : " + Math.round(65.4));
    System.out.printf(" 값 : %5.2f\n", Math.floor(65.9));
    System.out.printf(" 값 : %7.3f\n", Math.ceil(65.9));
  }
}