public class Print {
  public static void main(String[] args) {
    System.out.println("1. println : 줄바꿈 있는 출력");
    System.out.print("2. print : 줄바꿈 없는 출력");
    System.out.print("3. print : 줄바꿈 없는 출력-2");
    System.out.printf("\n4. printf : 양식 지정 %d", 4);
    System.out.printf("\n5. : 파이값은 %f 입니다.", 3.14);
    System.out.printf("\n6. : 파이값은 %5.3f 입니다.", 3.14);
    System.out.printf("\n7. : 파이값은 %e 입니다.", 3.1415927854);
  }
}
